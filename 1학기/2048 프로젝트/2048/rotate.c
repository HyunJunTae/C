#include <stdio.h>

int b[4][4] = {0};


void rotate(int b[4][4]){
	int a[4][4];
	int i, j;

	for (i=0;i<4;i++) {
		for (j=0;j<4;j++) {
			a[i][j] = b[i][j];
		}
	}

	b[0][0] = a[3][0];
	b[0][1] = a[2][0];
	b[0][2] = a[1][0];
	b[0][3] = a[0][0];
	b[1][0] = a[3][1];
	b[1][1] = a[2][1];
	b[1][2] = a[1][1];
	b[1][3] = a[0][1];
	b[2][0] = a[3][2];
	b[2][1] = a[2][2];
	b[2][2] = a[1][2];
	b[2][3] = a[0][2];
	b[3][0] = a[3][3];
	b[3][1] = a[2][3];
	b[3][2] = a[1][3];
	b[3][3] = a[0][3];

}



int main(void) {

    b[0][0] = 1;
	b[0][1] = 2;
	b[0][2] = 3;
	b[0][3] = 4;
	b[1][0] = 5;
	b[1][1] = 6;
	b[1][2] = 7;
	b[1][3] = 8;
	b[2][0] = 9;
	b[2][1] = 10;
	b[2][2] = 11;
	b[2][3] = 12;
	b[3][0] = 13;
	b[3][1] = 14;
	b[3][2] = 15;
	b[3][3] = 16;

    int i, j;

    printf("Before rotate\n");
    for (i=0;i<4;i++) {
        for(j=0;j<4;j++) {
            printf("%d ", b[i][j]);
        }
        printf("\n");
    }
    printf("--------------------------------\n");

    rotate(b);

    printf("After rotate\n");
    for (i=0;i<4;i++) {
        for(j=0;j<4;j++) {
            printf("%d ", b[i][j]);
        }
        printf("\n");
    }

    return 0;

}